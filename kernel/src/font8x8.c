#define FONT_TABLE_LEN (sizeof(font_table) / sizeof(font_table[0]))

const uint8_t *font8x8_get_glyph(char c) {
    if (c >= 'a' && c <= 'z') {
        c = (char)(c - 32); /* Milestone 0 renders lowercase as uppercase glyphs */
    }

    for (unsigned i = 0; i < FONT_TABLE_LEN; i++) {
        if (font_table[i].c == c) {
            return font_table[i].rows;
        }
    }
    return glyph_fallback;
}
